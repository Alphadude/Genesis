public class AreaClas {
    
    public static double Area(double x){
        double area;
        area = x;
        return area;
    }
    
    public static double Area(double x, double y){
        double area;
        area = x * y;
        return area;
    }
    public static double Area(double x, double y, double z){
        double area;
        area = x * y *z;
        return area;
    }
    public static double Area(double x, double y, double z, double a){
        double area;
        area = x * y *z * a;
        return area;
    }
    
}
